n1 = float(input('Digite sua nota: '))
n2 = float(input('Digite sua segunda nota: '))
m = (n1 + n2)/2
print('Sua média foi {:.1f}'.format(m))
if m >= 6.0:
    print('Sua média foi boa, PARABENS')#Sao blocos de comando
else:
    print('Sua média esta ruim, ESTUDE MAIS')#Sao blocos de comando
